export function addTwoNumbers ( nun1: number , nun2: number ): number {
    return nun1 + nun2;
}


export function substractTwoNumbers ( nun1: number , nun2: number ): number {
    return nun1 - nun2;
}

export function timesNumbers ( nun1: number , nun2: number ): number {
    return nun1 * nun2;
}

export function divideTwoNumbers ( nun1: number , nun2: number ): number {
    return nun1 / nun2;
}
